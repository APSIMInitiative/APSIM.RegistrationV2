
namespace APSIM.RegistrationV2.Components.Classes;

/// <summary>
/// Enum that follows the bootstrap alert types. 
/// Used to determine the colour of the alert and the icon that is shown.
/// </summary>
/// <remarks>
/// see for more details: https://getbootstrap.com/docs/5.0/components/alerts/#examples
/// </remarks>
public enum AlertType
{
    Primary,
    Secondary,
    Success,
    Danger,
    Warning,
    Info,
    Light,
    Dark
}